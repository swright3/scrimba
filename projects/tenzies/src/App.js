import React from "react"
import Die from "./Die"
import {nanoid} from "nanoid"
import Confetti from "react-confetti"

export default function App() {

    const [dice, setDice] = React.useState(allNewDice())
    const [tenzies, setTenzies] = React.useState(false)
    const [rolls, setRolls] = React.useState(0)
    const [dots, setDots] = React.useState(true)
    const [seconds, setSeconds] = React.useState(0)
    
    React.useEffect(() => {
        const allHeld = dice.every(die => die.isHeld)
        const firstValue = dice[0].value
        const allSameValue = dice.every(die => die.value === firstValue)
        if (allHeld && allSameValue) {
            setTenzies(true)
        }
    }, [dice])

    React.useEffect(() => {
      const interval = setInterval(() => setSeconds(prevSeconds => prevSeconds+1), 1000)
      return () => clearInterval(interval)
    }, [])

    function generateNewDie() {
        return {
            value: Math.ceil(Math.random() * 6),
            isHeld: false,
            id: nanoid()
        }
    }
    
    function allNewDice() {
        const newDice = []
        for (let i = 0; i < 10; i++) {
            newDice.push(generateNewDie())
        }
        return newDice
    }
    
    function rollDice() {
        if(!tenzies) {
            setRolls(prevRolls => prevRolls + 1)
            setDice(oldDice => oldDice.map(die => {
                return die.isHeld ? 
                    die :
                    generateNewDie()
            }))
        } else {
            setTenzies(false)
            setRolls(0)
            setDice(allNewDice())
            setSeconds(0)
        }
    }
    
    function holdDice(id) {
        setDice(oldDice => oldDice.map(die => {
            return die.id === id ? 
                {...die, isHeld: !die.isHeld} :
                die
        }))
    }

    function toggleDots() {
      setDots(prevDots => !prevDots)
    }
    
    const diceElements = dice.map(die => (
        <Die 
            key={die.id} 
            value={die.value} 
            isHeld={die.isHeld} 
            holdDice={() => holdDice(die.id)}
            dots={dots}
        />
    ))

    function formatSeconds(secs) {
      return `${Math.floor(secs/60)}:${secs%60}`
    }

    const sliderStyle = {float: dots ? "left" : "right"}
    
    return (
        <main>
            {tenzies && <Confetti />}
            <button className="dot-slider" onClick={toggleDots}>
              <div className="dot-slider-inner" style={sliderStyle}></div>
            </button>
            <h1 className="title">Tenzies</h1>
            <p className="instructions">Roll until all dice are the same. 
            Click each die to freeze it at its current value between rolls.</p>
            <div className="dice-container">
                {diceElements}
            </div>
            <button 
                className="roll-dice" 
                onClick={rollDice}
            >
                {tenzies ? "New Game" : "Roll"}
            </button>
            <section className='stats'>
              <h2 className="rollCounter">Rolls: {rolls}</h2>
              <h2 className='timeCounter'>Time: {formatSeconds(seconds)}</h2>
            </section>
        </main>
    )
}